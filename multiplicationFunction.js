//Write a function that takes two numbers as arguments and returns their product.

const multiplication = function (a, b) {
  return a * b;
};

const multiplicationArrowFunction = (a, b) => a * b;
function multiplicationFunction(a, b) {
  return a * b;
}

console.log(multiplication(2, 4));
console.log(multiplicationArrowFunction(3, 6));
console.log(multiplicationFunction(9, 3));
