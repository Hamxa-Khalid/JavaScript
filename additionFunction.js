//Write a function that takes two numbers as arguments and returns their sum.

//Name less Function
const nameLess = function (a, b) {
  return a + b;
};

function Sum(a, b) {
  return a + b;
}

const callBackSum = (a, b) => {
  return a + b;
};

console.log(nameLess(1, 2));
console.log(Sum(2, 6));
console.log(callBackSum(9, 3));
